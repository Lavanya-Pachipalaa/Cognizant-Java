public class DataType{
    public static void main(String[] args) {
        int i = 42;
        float f = 3.14f;
        double d = 99.99;
        char c = 'A';
        boolean b = true;
        System.out.println("Integer value (int): " + i);
        System.out.println("Float value (float): " + f);
        System.out.println("Double value (double): " + d);
        System.out.println("Character value (char): " + c);
        System.out.println("Boolean value (boolean): " + b);
    }
}
